void	ft_putchar(char c);

int		main(int ac, char **av)
{
	int	i;
	int	n;

	i = 1;
	while (i < ac)
	{
		n = 0;
		while (av[i][n])
		{
			ft_putchar(av[i][n]);
			n++;
		}
		ft_putchar('\n');
		i++;
	}
	return (0);
}
